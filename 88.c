#include <stdio.h>

int main(){
	int n1[4], n2[4];
	int a=0, b=0;

	scanf("%d%d", &n1[3], &n2[3]);
	
	n1[0] = n1[3]/1000;
	n1[1] = (n1[3] - 1000*n1[0]) / 100;
	n1[2] = (n1[3] - 1000*n1[0] - 100*n1[1]) / 10;
	n1[3] = n1[3] - 1000*n1[0] - 100*n1[1] - 10*n1[2];

	n2[0] = n2[3]/1000;
	n2[1] = (n2[3] - 1000*n2[0]) / 100;
	n2[2] = (n2[3] - 1000*n2[0] - 100*n2[1]) / 10;
	n2[3] = n2[3] - 1000*n2[0] - 100*n2[1] - 10*n2[2];

	#ifdef DEBUG
	printf("%d %d %d %d", n1[0], n1[1], n1[2], n1[3]);
	printf("%d %d %d %d", n2[0], n2[1], n2[2], n2[3]);
	#endif

	for(int i=0;i<4;i++){
		if(n1[i]==n2[i]){
			a++;
		}
		else{
			for(int j=0;j<4;j++){
				if((n1[i]==n2[j])&&(j!=i)){
					b++;
				}
			}
		}
	}
	printf("%dA%dB", a, b);
	return 0;
}